function Usuario(nombre, apellido, cedula,
    correo, user, clave, rol,
    semestre, jornada, paralelo) {
    this.nombre = nombre;
    this.apellido = apellido;
    this.cedula = cedula;
    this.correo = correo;
    this.user = user;
    this.clave = clave;
    this.rol = rol;
    this.semestre = semestre;
    this.jornada = jornada;
    this.paralelo = paralelo;
}
